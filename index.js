function transportName(bike, car, bus, train) {
    if (bike === void 0) { bike = 1; }
    if (car === void 0) { car = 3; }
    if (bus === void 0) { bus = 2; }
    if (train === void 0) { train = 1; }
    var number = bike + car + bus + train;
    return number;
}
console.log(transportName(2, 3, 1, 4));
